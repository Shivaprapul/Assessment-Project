/**
 * Get career by ID
 */
export function getCareerById(careerId: string): CareerData | undefined {
  return CAREER_CATALOG.find(career => career.id === careerId);
}

/**
 * Get careers by skill signals
 */
export function getCareersBySkillSignals(skillSignals: string[]): CareerData[] {
  return CAREER_CATALOG.filter(career =>
    career.skillSignals.some(signal => skillSignals.includes(signal))
  );
}

/**
 * Get all careers
 */
export function getAllCareers(): CareerData[] {
  return CAREER_CATALOG;
}

